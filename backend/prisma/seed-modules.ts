async function seedModules() {
  console.log('ðŸš€ Starte ModuleType Seeding...\n');

  for (const moduleData of moduleTypes) {
    const existing = await prisma.moduleType.findUnique({
      where: { name: moduleData.name },
    });

    if (existing) {
      console.log(`  â­ï¸  ${moduleData.name} existiert bereits, ueberspringe...`);
      continue;
    }

    await prisma.moduleType.create({
      data: moduleData,
    });

    console.log(`  âœ… ${moduleData.name} erstellt`);
  }

  console.log('\nâœ¨ ModuleType Seeding abgeschlossen!');
  console.log(`   ${moduleTypes.length} Module definiert`);

  // Statistik ausgeben
  const counts = await prisma.moduleType.groupBy({
    by: ['category'],
    _count: true,
  });

  console.log('\nðŸ“Š Module nach Kategorie:');
  for (const c of counts) {
    console.log(`   ${c.category}: ${c._count}`);
  }
}

seedModules()
  .catch((e) => {
    console.error('âŒ Seed-Fehler:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
